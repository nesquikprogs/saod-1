#include <iostream>
#include <vector>

/*
 * Лабораторная работа: Динамическое программирование
 * Задача: построение треугольника Паскаля.
 *
 * Условие:
 *  - На вход подаётся одно число N (0 <= N <= 30).
 *  - Нужно вывести N строк треугольника Паскаля.
 *  - Все числа при данных ограничениях помещаются в тип long long (long int).
 *
 * Реализация использует идею динамического программирования:
 *  значение в ячейке (i, j) выражается через уже посчитанные значения
 *  предыдущей строки:
 *
 *      C(i, 0) = C(i, i) = 1
 *      C(i, j) = C(i - 1, j - 1) + C(i - 1, j),  0 < j < i
 *
 *  Мы последовательно строим строки сверху вниз, поэтому все необходимые
 *  значения к моменту вычисления уже известны.
 */

/**
 * Функция строит первые n строк треугольника Паскаля.
 *
 * @param n  количество строк (0 <= n <= 30).
 * @return   вектор из n строк, где каждая строка — вектор коэффициентов.
 *
 * Алгоритм:
 *  1. Создаём внешний вектор размера n.
 *  2. Для каждой строки i (0..n-1) создаём вектор длины i+1.
 *  3. Первую и последнюю позицию в строке заполняем единицами.
 *  4. Внутренние элементы считаем по формуле:
 *          triangle[i][j] = triangle[i - 1][j - 1] + triangle[i - 1][j]
 */
std::vector<std::vector<long long>> buildPascalsTriangle(int n) {
    std::vector<std::vector<long long>> triangle;
    triangle.reserve(n);

    for (int i = 0; i < n; ++i) {
        // Строка содержит i + 1 элемент
        std::vector<long long> row(i + 1);

        // Первый и последний элементы строки равны 1
        row[0] = 1;
        row[i] = 1;

        // Промежуточные элементы вычисляем на основе предыдущей строки
        for (int j = 1; j < i; ++j) {
            row[j] = triangle[i - 1][j - 1] + triangle[i - 1][j];
        }

        triangle.push_back(row);
    }

    return triangle;
}

/**
 * Функция выводит треугольник Паскаля в стандартный вывод.
 *
 * Формат вывода:
 *  - Каждая строка треугольника выводится на отдельной строке.
 *  - Числа внутри строки разделяются одним пробелом.
 */
void printTriangle(const std::vector<std::vector<long long>>& triangle) {
    for (std::size_t i = 0; i < triangle.size(); ++i) {
        const auto& row = triangle[i];
        for (std::size_t j = 0; j < row.size(); ++j) {
            if (j > 0) {
                std::cout << ' ';
            }
            std::cout << row[j];
        }
        std::cout << '\n';
    }
}

int main() {
    int N;

    // Читаем N из стандартного ввода.
    // При необходимости можно перенаправить ввод из файла INPUT.TXT:
    //     program.exe < INPUT.TXT
    if (!(std::cin >> N)) {
        return 0;  // Некорректный ввод — прекращаем работу.
    }

    if (N < 0 || N > 30) {
        // В условии указано ограничение 0 <= N <= 30.
        // В учебной задаче можно просто ничего не выводить.
        return 0;
    }

    // Строим треугольник Паскаля с использованием динамического программирования.
    auto triangle = buildPascalsTriangle(N);

    // Выводим результат.
    printTriangle(triangle);

    return 0;
}

